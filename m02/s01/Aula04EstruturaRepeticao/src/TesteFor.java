public class TesteFor {
    public static void main(String[] args) {
        int valorFinal = 10;

        for (int i = 0; i < valorFinal; i++){
            System.out.println(i);
        }


        String nome = "Marcelo";
        for (int i = 0; i < nome.length(); i++){
            System.out.println(nome.charAt(i));
        }

    }
}
